use std::{
    env, fs,
    path::{Path, PathBuf},
};

use anyhow::{Context, Result, anyhow, bail};
use proc_macro2::TokenStream;
use xsd_parser::{
    Config,
    config::{BoxFlags, GeneratorFlags, Schema},
    models::code::{Module, SubModules},
    pipeline::renderer::NamespaceSerialization,
};

const VERSIONS: &[Version] = &[
    Version {
        schema: "1.4",
        module: "v1_4",
    },
    Version {
        schema: "1.5",
        module: "v1_5",
    },
    Version {
        schema: "1685-2009",
        module: "v2009",
    },
    Version {
        schema: "1685-2014",
        module: "v2014",
    },
    Version {
        schema: "1685-2022",
        module: "v2022",
    },
];

fn main() -> Result<()> {
    let args = Args::parse(env::args().skip(1))?;

    for version in args.versions()? {
        generate_version(&args, version)?;
    }

    Ok(())
}

fn generate_version(args: &Args, version: Version) -> Result<()> {
    let schema_path = args.schema_dir.join(version.schema).join("index.xsd");
    let out_dir = args.out_root.join(version.module).join("types");
    let config = Config::default()
        .with_schema(Schema::File(schema_path))
        .with_derive(["Debug", "Clone", "PartialEq"])
        .with_any_type_support()
        .with_qname_type()
        .with_xs_any_simple_type("xsd_parser_types::xml::AnySimpleType")
        .with_type_postfix("XsdType")
        .with_element_postfix("Element")
        .with_box_flags(BoxFlags::ENUM_ELEMENTS)
        .with_generator_flags(
            GeneratorFlags::USE_SCHEMA_MODULES | GeneratorFlags::ABSOLUTE_PATHS_INSTEAD_USINGS,
        )
        .with_quick_xml_config(NamespaceSerialization::Global, None, true);

    let module = xsd_parser::generate_modules(config).with_context(|| {
        format!(
            "failed to generate IP-XACT {} modules with xsd-parser",
            version.schema
        )
    })?;

    if out_dir.exists() {
        fs::remove_dir_all(&out_dir)
            .with_context(|| format!("failed to clear {}", out_dir.display()))?;
    }
    fs::create_dir_all(&out_dir)
        .with_context(|| format!("failed to create {}", out_dir.display()))?;

    module
        .write_to_files_with(|module, path| write_module(module, path, &out_dir, version))
        .with_context(|| format!("failed to write generated modules to {}", out_dir.display()))?;

    fs::write(
        args.out_root.join(version.module).join("mod.rs"),
        format!(
            "//! IEEE {} generated schema module.\n\npub mod types;\n",
            version.schema
        ),
    )
    .with_context(|| format!("failed to write {}/mod.rs", version.module))?;

    println!("wrote {}", out_dir.display());
    Ok(())
}

fn write_module(module: &Module, path: &Path, out_dir: &Path, version: Version) -> Result<()> {
    let filename = if module.modules.is_empty() {
        out_dir.join(path).with_extension("rs")
    } else {
        out_dir.join(path).join("mod.rs")
    };

    fs::create_dir_all(
        filename
            .parent()
            .context("generated output path has no parent directory")?,
    )?;

    let mut tokens = TokenStream::new();
    module.to_code(&mut tokens, SubModules::Files);
    let syntax = syn::parse2(tokens).context("generated tokens were not a valid Rust file")?;

    let mut code = String::from("// @generated by ipxact-codegen; do not edit by hand.\n");
    code.push_str(&format!(
        "// Source schema: crates/ipxact/schema/{}/index.xsd\n\n",
        version.schema
    ));
    code.push_str("#![allow(dead_code, unused_mut, unused_variables, clippy::all)]\n\n");
    code.push_str(&prettyplease::unparse(&syntax));
    apply_generated_fixes(version, path, &mut code);

    fs::write(&filename, code)
        .with_context(|| format!("failed to write {}", filename.display()))?;
    Ok(())
}

fn apply_generated_fixes(version: Version, path: &Path, code: &mut String) {
    if (version.schema == "1685-2014" || version.schema == "1685-2022")
        && path == Path::new("model").join("quick_xml_deserialize")
    {
        code.push_str(
            r#"

impl Default for ModelTypeInstantiationsInstantiationsGroupXsdTypeDeserializer {
    fn default() -> Self {
        Self {
            content: None,
            state__: Box::new(ModelTypeInstantiationsInstantiationsGroupXsdTypeDeserializerState::Init__),
        }
    }
}
"#,
        );
    }

    if version.schema == "1685-2022" && path == Path::new("memory_map") {
        *code = code.replacen(
            "pub type BankElement = AddressBankTypeXsdType;",
            "pub type AddressBankElement = AddressBankTypeXsdType;",
            1,
        );
    }

    if version.schema == "1685-2022" && path == Path::new("port") {
        remove_duplicate_named_block(
            code,
            "#[derive(Clone, Debug, Default, PartialEq)]\npub struct NameList",
            "impl xsd_parser_types::quick_xml::WithDeserializerFromBytes for NameList {}\n",
        );
    }
}

fn remove_duplicate_named_block(code: &mut String, start_marker: &str, end_marker: &str) {
    let Some(first_start) = code.find(start_marker) else {
        return;
    };
    let Some(second_start) = code[first_start + start_marker.len()..].find(start_marker) else {
        return;
    };
    let second_start = first_start + start_marker.len() + second_start;
    let Some(relative_end) = code[second_start..].find(end_marker) else {
        return;
    };
    let second_end = second_start + relative_end + end_marker.len();
    code.replace_range(second_start..second_end, "");
}

#[derive(Clone, Copy, Debug)]
struct Version {
    schema: &'static str,
    module: &'static str,
}

impl Version {
    fn parse(value: &str) -> Result<Self> {
        VERSIONS
            .iter()
            .copied()
            .find(|version| version.schema == value || version.module == value)
            .ok_or_else(|| anyhow!("unknown version: {value}"))
    }
}

#[derive(Debug)]
struct Args {
    schema_dir: PathBuf,
    out_root: PathBuf,
    versions: Vec<String>,
}

impl Args {
    fn parse<I>(args: I) -> Result<Self>
    where
        I: IntoIterator<Item = String>,
    {
        let mut schema_dir = PathBuf::from("crates/ipxact/schema");
        let mut out_root = PathBuf::from("crates/ipxact/src");
        let mut versions = Vec::new();

        let mut args = args.into_iter();
        while let Some(arg) = args.next() {
            match arg.as_str() {
                "--schema-dir" => {
                    schema_dir = PathBuf::from(next_value(&mut args, "--schema-dir")?);
                }
                "--out-dir" => {
                    out_root = PathBuf::from(next_value(&mut args, "--out-dir")?);
                }
                "--version" => {
                    versions = next_value(&mut args, "--version")?
                        .split(',')
                        .map(str::trim)
                        .filter(|item| !item.is_empty())
                        .map(ToOwned::to_owned)
                        .collect();
                }
                "--help" | "-h" => {
                    print_help();
                    std::process::exit(0);
                }
                _ => bail!("unknown argument: {arg}"),
            }
        }

        Ok(Self {
            schema_dir,
            out_root,
            versions,
        })
    }

    fn versions(&self) -> Result<Vec<Version>> {
        if self.versions.is_empty() {
            Ok(VERSIONS.to_vec())
        } else {
            self.versions
                .iter()
                .map(|version| Version::parse(version))
                .collect()
        }
    }
}

fn next_value(args: &mut impl Iterator<Item = String>, flag: &str) -> Result<String> {
    args.next()
        .with_context(|| format!("{flag} requires a value"))
}

fn print_help() {
    println!(
        "\
Generate IP-XACT Rust modules using xsd-parser.

Usage:
  cargo run -p ipxact-codegen -- [--schema-dir crates/ipxact/schema] \\
                                 [--out-dir crates/ipxact/src] \\
                                 [--version 1.4|1.5|1685-2009|1685-2014|1685-2022|v1_4|v1_5|v2009|v2014|v2022|comma-list]
"
    );
}
